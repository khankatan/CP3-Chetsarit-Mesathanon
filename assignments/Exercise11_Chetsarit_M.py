print(">>>>>>>>>>>>>>> มาสร้างพีระมิดกันเถอะ <<<<<<<<<<<<<<<")
level = int(input("จำนวนชั้นของพีระมิด : "))
if level >= 2:
    for p in range(level):
        print(" "*(level-p),"*"*((p-1)+(p+2)))
    print(">>>>>>>>>>>>>>> เย่!! สร้างเสร็จแล้ว <<<<<<<<<<<<<<<")
else:
    print(">>>>>>>>>>>>>>> พีระมิดต้องมี 2 ชั้นขึ้นไป <<<<<<<<<<<<<<<")